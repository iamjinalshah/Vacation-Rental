package digimation.vacationrental.bean;

public class AmenitiesBean {

		private int amenitiesId;
		private String amenitiesName;
		private int property_id;
		private String[] amenitiesNames;
		
		public int getAmenitiesId() {
			return amenitiesId;
		}
		public void setAmenitiesId(int amenitiesId) {
			this.amenitiesId = amenitiesId;
		}
		public String getAmenitiesName() {
			return amenitiesName;
		}
		public void setAmenitiesName(String amenitiesName) {
			this.amenitiesName = amenitiesName;
		}
		public int getProperty_id() {
			return property_id;
		}
		public void setProperty_id(int property_id) {
			this.property_id = property_id;
		}
		public String[] getAmenitiesNames() {
			return amenitiesNames;
		}
		public void setAmenitiesNames(String[] amenitiesNames) {
			this.amenitiesNames = amenitiesNames;
		}
		
		
}
